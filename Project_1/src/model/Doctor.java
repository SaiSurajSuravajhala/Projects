package model;

/**
 *
 * @author HP
 */
public class Doctor {
    private String name;
    private String doctorsId;
    private String specialisedIn;
    private String phoneNo;
    private String address;
    private String email;
    private String website;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDoctorsId() {
        return doctorsId;
    }

    public void setDoctorsId(String doctorsId) {
        this.doctorsId = doctorsId;
    }

    public String getSpecialisedIn() {
        return specialisedIn;
    }

    public void setSpecialisedIn(String specialisedIn) {
        this.specialisedIn = specialisedIn;
    }

    public String getPhoneNo() {
        return phoneNo;
    }

    public void setPhoneNo(String phoneNo) {
        this.phoneNo = phoneNo;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }
    
    
    
}
